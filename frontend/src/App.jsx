import { useState } from "react";

function App() {
  const [message, setMessage] = useState("");
  const [answer, setAnswer] = useState("");
  const [loading, setLoading] = useState(false);

  async function sendMessage() {
    if (!message.trim()) return;

    setLoading(true);

    try {
      const response = await fetch("http://127.0.0.1:8000/chat", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          user_id: "test_user",
          message: message,
        }),
      });

      const data = await response.json();

      setAnswer(data.answer);
    } catch (err) {
      setAnswer("Unable to connect to backend.");
      console.error(err);
    }

    setLoading(false);
  }

  return (
    <div
      style={{
        maxWidth: "900px",
        margin: "40px auto",
        fontFamily: "Arial",
      }}
    >
      <h1>🤖 TEF Chatbot</h1>

      <textarea
        rows="6"
        style={{
          width: "100%",
          padding: "12px",
          fontSize: "16px",
        }}
        placeholder="Ask a question..."
        value={message}
        onChange={(e) => setMessage(e.target.value)}
      />

      <br />
      <br />

      <button
        onClick={sendMessage}
        style={{
          padding: "10px 20px",
          fontSize: "16px",
          cursor: "pointer",
        }}
      >
        Ask
      </button>

      <hr />

      <h2>Answer</h2>

      {loading ? (
        <p>Thinking...</p>
      ) : (
        <div
          style={{
            background: "#f3f3f3",
            padding: "20px",
            borderRadius: "8px",
            whiteSpace: "pre-wrap",
          }}
        >
          {answer}
        </div>
      )}
    </div>
  );
}

export default App;
